package com.android.zmark.entities;

/**
 * 类名 ApkGrade.java 时间 2012-7-23 下午2:57:34 描述 apk升级实体
 */
public class ApkGrade {
	private String apkUrl; // APK地址
	private boolean isCompulsory; // 是否强制更新
	private int verCode; // 版本号码
	private String verName; // 版本名称
	private String updateInfo; // 升级信息

	/*
	 * @return the apkUrl
	 */
	public String getApkUrl() {
		return apkUrl;
	}

	/*
	 * @param apkUrl the apkUrl to set
	 */
	public void setApkUrl(String apkUrl) {
		this.apkUrl = apkUrl;
	}

	/*
	 * @return the isCompulsory
	 */
	public boolean isCompulsory() {
		return isCompulsory;
	}

	/*
	 * @param isCompulsory the isCompulsory to set
	 */
	public void setCompulsory(boolean isCompulsory) {
		this.isCompulsory = isCompulsory;
	}

	/*
	 * @return the verCode
	 */
	public int getVerCode() {
		return verCode;
	}

	/*
	 * @param verCode the verCode to set
	 */
	public void setVerCode(int verCode) {
		this.verCode = verCode;
	}

	/*
	 * @return the verName
	 */
	public String getVerName() {
		return verName;
	}

	public void setVerName(String verName) {
		this.verName = verName;
	}

	/*
	 * @return the updateInfo
	 */
	public String getUpdateInfo() {
		return updateInfo;
	}

	/*
	 * @param updateInfo the updateInfo to set
	 */
	public void setUpdateInfo(String updateInfo) {
		this.updateInfo = updateInfo;
	}

}